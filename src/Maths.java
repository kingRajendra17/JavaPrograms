import java.math.*;

public class Maths {

	public static void main(String[] args) {
		
		double x = -2.5;
		int y = 30;
		
		System.out.println("Random Number : " + Math.random());  // Changes Everytime
		System.out.println("Max of x & y : " + Math.max(x, y));
		System.out.println("Min of x & y : " + Math.min(x, y));
		
		System.out.println("Power of x & y : " + Math.pow(x, y));
		
		System.out.println("Square Root of y : " + Math.sqrt(y));
		
		System.out.println("Cube Root of y : " + Math.cbrt(y));
		
		System.out.println("Absolute of x : " + Math.abs(x));
		
		System.out.println("Round of x : " + Math.round(x));
		System.out.println("Ceil of x : " + Math.ceil(x));
		System.out.println("Floor of x : " + Math.floor(x));
		
		System.out.println("Log of y  : " + Math.log(y));
		System.out.println("Log10 of y  : " + Math.log10(y));
		
		System.out.println("Sin of x : " + Math.sin(x));
		System.out.println("Cos of x : " + Math.cos(x));
		System.out.println("Tan of x : " + Math.tan(x));
		
	}}
